package uat;

public class UserInfo {

    private String username;
    private String password;
    private String name;
    private String question;
    private String answer;
    private int negativeDepositAmount;
    private int positiveDepositAmount;
    private int negativeWithdrawAmount;
    private int positiveAboveBalanceWithdrawAmount;
    private int positiveBelowBalanceWithdrawAmount;
    
    public String getUsername() {
        return username;
    }
    
    public void setUsername(String username) {
        this.username = username;
    }
    
    public String getPassword() {
        return password;
    }
    
    public void setPassword(String password) {
        this.password = password;
    }
    
    public String getName() {
        return name;
    }
    
    public void setName(String name) {
        this.name = name;
    }
    
    public String getQuestion() {
        return question;
    }
    
    public void setQuestion(String question) {
        this.question = question;
    }
    
    public String getAnswer() {
        return answer;
    }
    
    public void setAnswer(String answer) {
        this.answer = answer;
    }
    
    public int getNegativeDepositAmount() {
        return negativeDepositAmount;
    }
    
    public void setNegativeDepositAmount(int negativeDepositAmount) {
        this.negativeDepositAmount = negativeDepositAmount;
    }
    
    public int getPositiveDepositAmount() {
        return positiveDepositAmount;
    }
    
    public void setPositiveDepositAmount(int positiveDepositAmount) {
        this.positiveDepositAmount = positiveDepositAmount;
    }
    
    public int getNegativeWithdrawAmount() {
        return negativeWithdrawAmount;
    }
    
    public void setNegativeWithdrawAmount(int negativeWithdrawAmount) {
        this.negativeWithdrawAmount = negativeWithdrawAmount;
    }
    
    public int getPositiveAboveBalanceWithdrawAmount() {
        return positiveAboveBalanceWithdrawAmount;
    }
    
    public void setPositiveAboveBalanceWithdrawAmount(int positiveAboveBalanceWithdrawAmount) {
        this.positiveAboveBalanceWithdrawAmount = positiveAboveBalanceWithdrawAmount;
    }
    
    public int getPositiveBelowBalanceWithdrawAmount() {
        return positiveBelowBalanceWithdrawAmount;
    }
    
    public void setPositiveBelowBalanceWithdrawAmount(int positiveBelowBalanceWithdrawAmount) {
        this.positiveBelowBalanceWithdrawAmount = positiveBelowBalanceWithdrawAmount;
    }
    
    
    
    
}
